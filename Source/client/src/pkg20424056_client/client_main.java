    private void btn_startchatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_startchatActionPerformed
        // TODO add your handling code here:
        if(list_users.isSelectionEmpty())
        {
            msg.showMessage("Warning", "Please Choose a user in list to chat!");
            return;
        }
        
        selectedUsername = list_users.getSelectedValue().toString();
        if(selectedUsername.equals(""))
        {
           return;
        }
        
        chat_window cwtmp = windowList.get(selectedUsername);
        if((cwtmp != null))
        {
            msg.showMessage("Warning", "Already chatting with this user!");
            return;
        }

        chat_window tmp = new chat_window(this, username, selectedUsername);
        windowList.put(selectedUsername, tmp);

        tmp.setVisible(true);
    }//GEN-LAST:event_btn_startchatActionPerformed

    private void hanldeLogout() throws IOException
    {
        if(windowList.isEmpty() == false)
        {
            msg.showMessage("Warning", "Please all chat windows before logout!");
            return;
        }
        if(msg.showYesNo("Logout","Are you sure you want to logout?"))
        {
            //send msg to server
            tcpc.closeConnect();
            //logout
            this.setVisible(false);
            cl.setVisible(true);
        }
    }
