    private void btn_refreshActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_refreshActionPerformed
        try {
            // TODO add your handling code here:
            if(tcpc.refresh() == true)
            {
                if(tcpc.onlineUsers == null)
                {
                    return;
                }
                if(tcpc.onlineUsers.length < 1)
                {
                    return;
                }
                
                list_users.removeAll();
                Thread.sleep(50);
                DefaultListModel model = new DefaultListModel();
                for(String user: tcpc.onlineUsers)
                {
                    model.addElement(user);
                }
                list_users.setModel(model);
            }
            else
            {
                msg.showMessage("Warning", "No respone data");
            }
                } catch (IOException ex) {
            Logger.getLogger(client_main.class.getName()).log(Level.SEVERE, null, ex);
        } catch (InterruptedException ex) {
            Logger.getLogger(client_main.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_refreshActionPerformed

